R = float(input("Радіус диска(в cм):"))
m = float(input("Маса диска(в кг):"))
d = float(input("Діаметр отвору(в cм):"))
a = float(input("Відстань від центра отвору до осі диска(в см):"))
a /= 100
R /= 100
r = d / 200

i = (m * R ** 4 - m * r ** 2 * (r ** 2 + 2 * a ** 2)) / (2 * R ** 2)
res_i = round(i, 3)
print(f"Момент інерції відносно осі, яка проходить через центр диска перпендикулярно до його площини {i} ≈ {res_i} кг×м²")