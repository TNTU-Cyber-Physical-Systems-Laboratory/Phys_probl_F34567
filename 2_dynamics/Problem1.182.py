import math

# Дано:
R = 150       # радіус повороту в метрах
mu = 0.7      # коефіцієнт тертя (мю)
g = 9.81      # прискорення вільного падіння на Землі

# Логіка така:
# Сила тертя (F = mu * m * g) має бути рівна доцентровій силі (F = m * v^2 / R).
# Маса (m) скорочується, і залишається формула: v^2 = mu * g * R
# Отже, максимальна швидкість v = sqrt(mu * g * R)

# Рахуємо швидкість
v_max = math.sqrt(mu * g * R)

# Переведемо також у км/год, бо для авто так звичніше
v_km_god = v_max * 3.6

# Вивід результатів
print("Максимальна швидкість:")
print(v_max, "м/с")
print(v_km_god, "км/год")