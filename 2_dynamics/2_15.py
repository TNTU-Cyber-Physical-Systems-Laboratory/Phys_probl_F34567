r = float(input("Радіус диска(в м):"))
m = float(input("Маса диска(в кг):"))
e = float(input("Кутове прискорення диска(в рад/с²):"))
y = float(input("Коефіцієнт тертя:"))
g = 9.8
t = ((g ** 2 * y ** 2 - e ** 2 * r ** 2) ** 0.5 * r) ** 0.5 / (e * r)
res_t = round(t, 3)
print(f"Час після початку руху, через який тіло зісковзне з поверхні диска {t} ≈ {res_t} c")