//10.22. Струм силою 20 А, протікаючи по дротяному кільцю з мідного дроту перерізом 1 мм2, створює у центрі кільця напруженість магнітного поля 178 А/м. Яка різниця потенціалів прикладена до кінців цього дроту ?
#include <iostream>
#include <math.h>
using namespace std;
int main()
{
    double I, S, H, ρ;
    const double PI = 3.141592653589793;
    cout << "Введіть силу струму I (А): ";
    cin >> I;
    if (I <= 0) {
        cout << "Помилка! Cила струму повинна бути додатною!\n";
        return 1;
    }
    if (I > 1000) {
        cout << "Помилка! Надто велика сила струму!\n";
        return 1;
    }
    cout << "Введіть площу перерізу дроту S (м^2, наприклад 1e-6): ";
    cin >> S;
    if (S <= 0) {
        cout << "Помилка! Площа перерізу повинна бути додатною!\n";
        return 1;
    }
    if (S > 1) {
        cout << "Помилка! Надто велика площа перерізу!\n";
        return 1;
    }
    cout << "Введіть напруженість магнітного поля H (А/м): ";
    cin >> H;
    if (H <= 0) {
        cout << "Помилка! Напруженість повинна бути додатною!\n";
        return 1;
    }
    if (H > 1e6) {
        cout << "Помилка! Надто велика напруженість!\n";
        return 1;
    }
    cout << "Введіть питомий опір дроту ρ (Ом·м, наприклад 1.7e-8): ";
    cin >> ρ;
    if (ρ <= 0) {
        cout << "Помилка! Питомий опір повинен бути додатним!\n";
        return 1;
    }
    if (ρ > 1) {
        cout << "Помилка! Надто великий питомий опір!\n";
        return 1;
    }
    double r = I / (2.0 * H);
    double L = 2.0 * PI * r;
    double R = ρ * L / S;
    double U = I * R;
    cout << "U = " << U << " В" << endl;

    return 0;
}
