ro = 1.75 / 100000000
i = float(input("Введіть значення сили струму, що протікає в кільці з мідного дроту(в А):"))
s = float(input("Введіть значення площі прерізу мідного дроту(в м²):"))
h = float(input("Введіть значення напруженості магнітного поля у центрі кільця(в А/м):"))
print(f"Різниця потенціалів прикладена до кінців цього дроту {round(i ** 2 * ro * 3.14 / s / h, 4)} В.")