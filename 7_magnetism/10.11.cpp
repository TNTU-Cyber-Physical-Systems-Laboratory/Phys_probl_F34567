#include <iostream>
#include <cmath>
#include <iomanip> 
#include <locale>
using namespace std;

int main() {
setlocale(LC_CTYPE, "ukr");
{
    // Вхідні дані (за умовою задачі)
    // Струм в обох провідниках, Ампери
    const double I = 5.0; 
    // Відстань від провідників до точки (рівносторонній трикутник), сантиметри
    const double d_cm = 10.0; 
    // Кут між векторами H1 та H2 (для рівностороннього трикутника та протилежних струмів)
    // Кут між радіусами до провідників 60 градусів.
    // Кут між H1 і H2: 180 - (60 + 60) = 60 градусів. 
    const double alpha_degrees = 60.0;

    // Перетворення одиниць
    // Відстань r в метри
    const double d_m = d_cm / 100.0; // 10 см = 0.1 м
    // Кут у радіани для cmath функцій
    const double alpha_radians = alpha_degrees * M_PI / 180.0;

    //Обчислення напруженості H0 від одного провідника
    // H0 = I / (2 * pi * d)
    const double H0 = I / (2.0 * M_PI * d_m);

    //Обчислення результуючої напруженості H
    // Формула векторного додавання: H = sqrt(H1^2 + H2^2 + 2*H1*H2*cos(alpha))
    // Оскільки H1 = H2 = H0, формула спрощується:
    // H = H0 * sqrt(3), тому що cos(60 deg) = 0.5
    
    // Перевірка повна формула (для гнучкості):
    double H_squared = pow(H0, 2) + pow(H0, 2) + 2.0 * H0 * H0 * cos(alpha_radians);
    double H = sqrt(H_squared);
    
    // Або спрощена формула (для перевірки):
    // double H_simplified = H0 * sqrt(3.0); 

    // Виведення результатів
    cout << "10.11" << endl;
    cout << "Струм (I1 = I2): " << I << " А" << endl;
    cout << "Відстань до точки (d1 = d2): " << d_m << " м" << endl;
    cout << "Кут між векторами H1 та H2: " << alpha_degrees << " град." << endl;
    
    cout << "Напруженість від одного провідника (H0): " << H0 << " А/м" << endl;
    cout << "Результуюча напруженість магнітного поля (H): " << H << " А/м" << endl;
    
    //Визначення напрямку
    cout << "Напрямок: " << endl;
    cout << "Напрямок результуючого вектора H лежить уздовж бісектриси " 
         << "кута між векторами H1 та H2, оскільки H1 = H2." << endl;
    cout << "У даному випадку, результуючий вектор H буде паралельний лінії, " 
         << "що з'єднує провідники, і спрямований від провідників." << endl;
}
return 0;
}
