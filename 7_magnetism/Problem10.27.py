import math

# Дано:
R = 0.04       # радіус (перевів см у метри)
d = 0.05        # відстань між витками (перевів см у метри)
I = 4          # струм у Амперах
mu0 = 4 * math.pi * 1e-7  # магнітна стала

# 1.Рахуємо власне поле витка у його центрі
B_vlasne = (mu0 * I) / (2 * R)

# 2. Рахуємо поле, яке створює сусідній виток у цій же точці
# Формула для поля на осі кільця на відстані d
chiselnik = mu0 * I * (R**2)
znamennyk = 2 * ( (R**2 + d**2)**(1.5) )
B_susidnie = chiselnik / znamennyk

# 3.Випадок 1: Струми в одному напрямку (поля додаються)
B_sum_однакові = B_vlasne + B_susidnie

#4.Випадок 2: Струми в протилежних напрямках (поля віднімаються)
B_sum_rizni = B_vlasne - B_susidnie

# Вивід результатів (у мікротеслах мкТл для зручності)
print("Власне поле витка:", B_vlasne * 1e6, "мкТл")
print("Поле від другого витка:", B_susidnie * 1e6, "мкТл")
print("-" * 30)
print("1) Струми в одному напрямку:", B_sum_однакові * 1e6, "мкТл")
print("2) Струми в різних напрямках:", B_sum_rizni * 1e6, "мкТл")