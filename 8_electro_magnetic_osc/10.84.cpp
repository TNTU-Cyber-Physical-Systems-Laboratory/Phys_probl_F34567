// задача 10.84
#include <iostream>
#include <cmath>
#include <locale>
using namespace std;

int main() {
setlocale(LC_CTYPE, "ukr");
{
    //Вхідні дані
    // Магнітна проникність осердя (безрозмірна)
    const double u_r = 1240.0;
    // Кількість витків
    const int N = 800;
    // Довжина соленоїда в метрах
    const double l = 1.0;
    // Площа поперечного перерізу в см^2
    const double S_cm2 = 10.0;
    // Сила струму в Амперах
    const double I = 1.0;

    // Фізичні константи
    // Магнітна стала (Гн/м), приблизно 4*pi*10^-7
    const double u_0 = 4.0 * M_PI * 1e-7;

    // Перетворення одиниць
    // Площа поперечного перерізу в м^2
    // 1 см^2 = 1e-4 м^2
    const double S_m2 = S_cm2 * 1e-4;

    //Обчислення індуктивності L
    // L = mu_0 * mu_r * (N^2 * S) / l
    double L = u_0 * u_r * (pow(N, 2) * S_m2) / l;

    //Обчислення енергії магнітного поля W
    // W = 0.5 * L * I^2
    double W = 0.5 * L * pow(I, 2);

    //Виведення результатів
    cout << "=== 10.84 ===" << endl;
    cout << "Магнітна проникність (u): " << u_r << endl;
    cout << " Кількість витків(N): " << N << endl;
    cout << "Довжина соленоїда  (l): " << l << "м" << endl;
    cout << " Площа перерізу(S): " << S_m2 << "м^2" << endl;
    cout << "Сила струму(I): " << I << "А" << endl;
    cout << fixed; // Встановлення фіксованого формату для виводу
    cout.precision(8); // Встановлення точності виводу

    cout << "Індуктивність соленоїда (L): " << L << "Гн" << endl;
    cout << "Енергія магнітного поля(W): " << W << "Дж" << endl;
    cout.precision(4); // Зменшення точності для кінцевої відповіді
    cout << "Відповідь: Енергія магнітного поля =" << W << "Дж" << endl;
}

    return 0;
}
