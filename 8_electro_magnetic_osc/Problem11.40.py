# Дано:
w = 0.5        # густина енергії (Дж/м^3)
t_hv = 2       # час у хвилинах
S_plosha = 1   # площа у м^2
c = 3e8        # швидкість світла у м/с (3 * 10^8)

# Переводимо час у секунди
t_sec = t_hv * 60

#1.Знаходимо модуль вектора Пойнтінга
# Формула: P = w * c
P = w * c

# 2.Знаходимо загальну кількість енергії (W)
# Енергія = Потік * Площа * Час
W_energy = P * S_plosha * t_sec

# Вивід результатів
print("1) Модуль вектора Пойнтінга P:")
print(f"{P:.1e} Вт/м^2") 

print("\n2) Кількість енергії, що переноситься:")
print(f"{W_energy:.1e} Дж")

# Для кращого розуміння виведемо в гігаджоулях
print(f"Або {W_energy / 1e9:.1f} ГДж")