import math


# Дано:
v_pochatkova = 10000  # швидкість 10 км/с перевів у м/с
visota = 10  # висота в метрах
dt = 0.0014  # інтервал часу 1.4 мс перевів у секунди

# Пояснення логіки:
# Використовуємо формулу для висоти: h = v0*t - g*t^2 / 2
# Оскільки тіло було на одній висоті двічі, різниця між цими моментами часу (dt)
# пов'язана з прискоренням g.
# Після виведення формул через дискримінант виходить таке рівняння:
# (dt^2 / 4) * g^2 + 2 * h * g - v0^2 = 0

# Коефіцієнти для квадратного рівняння (ax^2 + bx + c = 0)
a = (dt ** 2) / 4
b = 2 * visota
c = -(v_pochatkova ** 2)

# Рахуємо дискримінант
diskr = b ** 2 - 4 * a * c

# Знаходимо g (корінь має бути додатнім, бо це прискорення)
g = (-b + math.sqrt(diskr)) / (2 * a)

# Виводимо результат
print("Прискорення вільного падіння на Сіріусі-В:")
print(g, "м/с^2")