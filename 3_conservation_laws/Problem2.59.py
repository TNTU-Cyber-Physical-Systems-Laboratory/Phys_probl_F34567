# Дано
m = 2          # маса тіла (кг)
A = 10         # метри
B = -2         # м/с
C = 1          # м/с^2
D = -0.2       # м/с^3

# Моменти часу, для яких треба знайти потужність
chasi = [2, 5]

for t in chasi:
    # 1. Рахуємо швидкість за формулою: v = x' = B + 2*C*t + 3*D*t^2
    v = B + 2 * C * t + 3 * D * t**2
    
    # 2. Рахуємо прискорення за формулою: a = v' = 2*C + 6*D*t
    a = 2 * C + 6 * D * t
    
    # 3. Шукаємо силу: F = m * a
    F = m * a
    
    # 4. Шукаємо потужність: N = F * v
    N = F * v
    
    # Вивід результатів для кожного часу
    print("Для часу t =", t, "с:")
    print("Швидкість v =", v, "м/с")
    print("Сила F =", F, "Н")
    print("Потужність N =", N, "Вт")
    print("-" * 20)