d = float(input("Введіть відстань центра мас вантажів від середини стержня(в м):"))
t = float(input("Введіть період малих коливань стержня з вантажами навколо горизонтальної осі, яка проходить через його середину(в с):"))
g = 9.8
l = round((t * (g * d)**0.5) / 3.14, 4)
print(f"Довжина стержня l ≈ {l} м.")