import math

# Дано
L = 0.8        # довжина нитки, перевів см у метри
a = 4          # горизонтальне прискорення машини (м/с^2)
g = 9.81       # звичайне прискорення вільного падіння

# 1. Знаходимо "ефективне" прискорення (результуюче)
# Воно діє на маятник через рух машини
g_eff = math.sqrt(g**2 + a**2)

# 2. Формула періоду маятника: T = 2 * pi * sqrt(L / g_eff)
period = 2 * math.pi * math.sqrt(L / g_eff)

# Вивід результатів
print("Ефективне прискорення g':", g_eff, "м/с^2")
print("Період коливань маятника T:", period, "с")