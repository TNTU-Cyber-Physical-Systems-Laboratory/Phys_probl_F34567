e = float(input("Введіть значення електрорушійної сили батарейки кишенькового ліхтаря(в В):"))
r = float(input("Введіть значення внутрішнього опору батарейки кишенькового ліхтаря(в Ом):"))
u = float(input("Введіть значення напруги, на яке розрахована лампа(в В):"))
p = float(input("Введіть значення потужності, на яке розрахована лампа(в Вт):"))
if e - p / u * r > 0:
    print(f"Потрібно з'єднати {int(u/(e - p / u * r)) + 1} батарейок.")
else:
    print("Введено хибні значення.")