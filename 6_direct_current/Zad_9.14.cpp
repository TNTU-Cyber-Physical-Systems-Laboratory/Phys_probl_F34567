//9.14. Електрорушійна сила батарейки кишенькового ліхтаря ε=4,5 В, а її внутрішній oпip r=3 Ом. Скільки таких батарейок треба з’єднати послідовно, щоб живити лампу, яка розрахована на напругу U=220 В i потужність Р=60 Вт ?
#include <iostream>
#include <math.h>
using namespace std;
int main()
{
    double U, P, ε, r;
    cout << "Введіть напругу лампи U (В): ";
    cin >> U;
    if (U <= 0) { cout << "Помилка! Напруга повинна бути додатною!\n"; return 1; }
    if (U > 1000) { cout << "Помилка! Напруга занадто висока!\n"; return 1; }
    cout << "Введіть потужність лампи P (Вт): ";
    cin >> P;
    if (P <= 0) { cout << "Помилка! Потужність повинна бути додатною!\n"; return 1; }
    if (P > 1000) { cout << "Помилка! Потужність занадто велика!\n"; return 1; }
    cout << "Введіть ЕРС батарейки ε (В): ";
    cin >> ε;
    if (ε <= 0) { cout << "Помилка! ЕРС повинна бути додатною!\n"; return 1; }
    if (ε > 50) { cout << "Помилка! Занадто велика ЕРС!\n"; return 1; }
    cout << "Введіть внутрішній опір батарейки r (Ом): ";
    cin >> r;
    if (r < 0) { cout << "Помилка! Опір не може бути від'ємним!\n"; return 1; }
    double R = pow(U, 2) / P;
    double I = U / R;
    if (ε - I * r <= 0) {
        cout << "Помилка! Неможливо живити лампу цими батарейками!:(\n";
        return 1;
    }
    double n_exact = U / (ε - I * r);
    int n = ceil(n_exact);
    cout << "n = " << n << " батарейок" << endl;
    return 0;
}
