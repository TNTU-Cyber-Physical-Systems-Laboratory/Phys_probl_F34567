# Дано:
U = 220        # напруга у Вольтах
P = 100        # потужність у Ватах
t1 = 2500      # температура розжарення
t2 = 10        # початкова температура
n = 15         # у скільки разів опір більший

# 1. Знаходимо опір лампи, коли вона світиться (гаряча)
# Формула: R = U^2 / P
R1 = (U**2) / P

# 2. Знаходимо опір при t2 = 10 градусів (холодна)
R2 = R1 / n

# 3. Шукаємо температурний коефіцієнт опору вольфраму (альфа)
# Формула: R1 = R2 * (1 + alpha * (t1 - t2))
# Звідси: alpha = (R1/R2 - 1) / (t1 - t2)
# Оскільки R1/R2 = n, формула спрощується:
alpha = (n - 1) / (t1 - t2)

# Вивід результатів
print("1) Опір при температурі 10°C (R2):")
print(R2, "Ом")

print("\n2) Температурний коефіцієнт опору (alpha):")
print(alpha, "К^-1 (або 1/градус)")